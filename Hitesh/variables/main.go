package main

import "fmt"

func main() {
	var my_var string = "Wasim"
	fmt.Printf("The value is: %v and the type is %T", my_var, my_var)

	var my_var1 int
	fmt.Printf("The value is: %v and the type is %T", my_var1, my_var1)

	var my_var2 string
	fmt.Printf("The value is: _%v_ and the type is %T", my_var2, my_var2)
}
